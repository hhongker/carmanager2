package com.jy.it.entity;

public class Rule {
	private int id;
	private String code;
	private String name;
	private int score;
	private int pay;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getPay() {
		return pay;
	}
	public void setPay(int pay) {
		this.pay = pay;
	}
	@Override
	public String toString() {
		return "Rule [id=" + id + ", code=" + code + ", name=" + name + ", score=" + score + ", pay=" + pay + "]";
	}
	
	
}
